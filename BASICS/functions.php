<?php
function add ($n1 , $n2) {
    return $n1 + $n2 ; 
} 
function sub ($n1 , $n2) {
    return $n1 - $n2 ; 
}
function mul ($n1 , $n2) {
    return $n1 * $n2 ; 
}
function dev ($n1 , $n2) {
    return $n1 / $n2 ; 
}
echo add(1,2) . "<br>" ; 
echo sub(3,4) . "<br>" ; 
echo mul(3,5) . "<br>" ; 
echo dev(9 , 4). "<br>" ; 
?>